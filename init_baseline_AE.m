function [pop, fit, detail] = init_baseline_AE(params, map, refCtrl)
%INIT_BASELINE_AE Baseline random initialization without feasibility-guided init.

N = params.popSize;
pop = zeros(N, params.dim);
fit = inf(N, 1);
detail = repmat(emptyDetailStruct(), N, 1);

for j = 1:N
    ctrl = refCtrl;

    % Random interior control points
    for i = 2:size(ctrl, 1)-1
        q = zeros(1,3);
        q(1) = params.lbSingle(1) + rand * (params.ubSingle(1) - params.lbSingle(1));
        q(2) = params.lbSingle(2) + rand * (params.ubSingle(2) - params.lbSingle(2));
        q(3) = params.lbSingle(3) + rand * (params.ubSingle(3) - params.lbSingle(3));
        ctrl(i, :) = q;
    end

    X = encodeControlPoints(ctrl);
    X = boundSolution(X, params);

    pop(j, :) = X;
    [fit(j), detail(j)] = fitnessFAEAE(X, map, params);
end
end

